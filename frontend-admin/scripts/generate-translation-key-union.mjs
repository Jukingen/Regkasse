/**
 * Flattens canonical locale JSON (de) into dot-path keys matching runtime t('namespace.a.b').
 * Emits src/i18n/generated/translationKeys.ts for compile-time key unions + runtime Set.
 */
import fs from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');
const DE_DIR = path.join(ROOT, 'src', 'i18n', 'locales', 'de');
const OUT = path.join(ROOT, 'src', 'i18n', 'generated', 'translationKeys.ts');

/**
 * JSON dosya adı → `getCatalog().de` anahtarı (bkz. src/i18n/config.ts).
 * Yeni namespace eklenince burayı ve config'i birlikte güncelleyin.
 */
function jsonFileStemToNamespace(stem) {
  if (stem === 'admin-shell') return 'adminShell';
  return stem;
}

/** @param {Record<string, unknown>} obj */
function flattenStringLeaves(obj, prefix = '') {
  /** @type {string[]} */
  const out = [];
  for (const [k, v] of Object.entries(obj)) {
    const p = prefix ? `${prefix}.${k}` : k;
    if (typeof v === 'string') {
      out.push(p);
    } else if (v !== null && typeof v === 'object' && !Array.isArray(v)) {
      out.push(...flattenStringLeaves(/** @type {Record<string, unknown>} */ (v), p));
    }
  }
  return out;
}

async function main() {
  const files = (await fs.readdir(DE_DIR)).filter((f) => f.endsWith('.json')).sort();
  /** @type {string[]} */
  const all = [];
  for (const file of files) {
    const ns = jsonFileStemToNamespace(path.basename(file, '.json'));
    const raw = await fs.readFile(path.join(DE_DIR, file), 'utf8');
    const json = JSON.parse(raw);
    const leaves = flattenStringLeaves(json);
    for (const leaf of leaves) {
      all.push(`${ns}.${leaf}`);
    }
  }
  const unique = [...new Set(all)].sort((a, b) => a.localeCompare(b));

  const header = `/**
 * AUTO-GENERATED by scripts/generate-translation-key-union.mjs — do not edit by hand.
 * Canonical paths from src/i18n/locales/de/*.json (string leaves only).
 * Regenerate: npm run i18n:keys
 */
`;

  const body = `${header}export const ADMIN_TRANSLATION_KEYS = [
${unique.map((k) => `  '${k.replace(/'/g, "\\'")}',`).join('\n')}
] as const;

export type AdminTranslationKey = (typeof ADMIN_TRANSLATION_KEYS)[number];

export const ADMIN_TRANSLATION_KEY_SET: ReadonlySet<string> = new Set(ADMIN_TRANSLATION_KEYS);
`;

  await fs.mkdir(path.dirname(OUT), { recursive: true });
  await fs.writeFile(OUT, body, 'utf8');
  console.log(`Wrote ${unique.length} keys to ${path.relative(ROOT, OUT)}`);
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
