/**
 * Shared CLI arg helpers for tools/i18n thin wrappers.
 * Canonical i18n tooling lives under localization/ — these wrappers only forward.
 */
export function parseArgs(argv) {
  const args = {};
  for (let i = 2; i < argv.length; i += 1) {
    const token = argv[i];
    if (!token.startsWith('--')) continue;
    const key = token.slice(2);
    const value = argv[i + 1] && !argv[i + 1].startsWith('--') ? argv[++i] : 'true';
    args[key] = value;
  }
  return args;
}
